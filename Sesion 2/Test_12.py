"""Listas en python"""


"""Listas remove(): Elimina un elemento de la lista indicando el valor"""

var_1 = ["Python", "java", "PHP", "Ruby", "Javascrip", "R studio"]

print("Mi lista var_1 es la siguiente: {}".format(var_1))

var_1.remove("Ruby")
var_1.remove("R studio")
print("Mi lista actualizada es: {}".format(var_1))
